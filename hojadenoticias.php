La Directora Ejecutiva del Observatorio de Violencia Familiar y de Género Cdra. Silvana Labat, charló con Canal 12 sobre la implementación del nuevo <b>Sistema Multiplataforma de Denuncia</b>  que permitirá unificar la información de las denuncias por violencia familiar y de género en una sola matriz.<br>
Este sistema será compartido por la Policía, la Línea 137, la Línea 102, la Defensoría de Niños, Niñas y Adolescentes y el Poder Judicial.<br><br>

La matriz única permitirá conocer (además de los datos de la víctima):<br>
<ul class="texto_ext lista_org">
<li><b>Quién</b> tomó la denuncia: qué organismo y agente del Estado</li>
<li><b>Hacia Dónde</b> fue esa denuncia, por ej. si fue a un juzgado y a cuál</li>
<li>Si hubo <b>intervención</b> de un organismo, por ej. la brigada móvil de la Línea 137</li>
<li>Si la víctima fue trasladada a una <b>casa refugio</b></li>
<li>Si hay <b>niñxs y adolescentes</b>. En este caso llegará automáticamente un alerta a la defensoría.<br><br></li>
</ul>
<p class="texto_ext">Se prevé un mecanismo de semáforo, que determinará la <b>urgencia y gravedad</b> del caso para actuar con prioridad. Ante la <b>primera denuncia</b>, se pondrá en marcha todo el dispositivo. No habrá que esperar a que se acumulen denuncias. </p>
<br><br>
<p class="texto_ext">Además la Directora habló sobre el Convenio que puso en marcha el <b>1er Registro de Personas Trans de Misiones</b>. El objetivo es conocer <i>dónde y cómo</i> viven para poder orientar las políticas públicas sin caer en estereotipos que circulan en el imaginario sobre esta comundidad.<br><br></p>
