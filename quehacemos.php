<div class='contenoti_ext'>
<p class='texto_ext'>El Observatorio de Violencia Familiar y de Género surge en septiembre del año 2019, dado que la violencia es considerada un problema social y complejo, su objetivo es avanzar hacia la igualdad de trato de todas las personas en la sociedad y el cumplimiento del rol que le compete al Estado en esta tarea; de igual manera el Instituto Provincial de Estadísticas y Censos “IPEC”, ya venía trabajando con datos sobre violencia desde el año 2016 como receptores de la información proveniente de las instituciones que brindan algún tipo de asistencia a la víctima dentro de su jurisdicción.
<br> <br>
El Observatorio de Violencia Familiar y de Género se encarga de desarrollar un sistema de información permanente con la finalidad de fomentar el diseño, implementación de estrategias para la identificación, tratamiento integral, prevención y erradicación de la violencia familiar y de género, además de organizar la recolección y sistematización de información conceptual permanente y conocimiento basado en el monitoreo, recopilación, registro, estudio, investigación, análisis, difusión y divulgación de datos sobre casos de violencia familiar y de género, junto a informes sobre todos los tipos y modalidades de violencia como así también de la (Ley N.º 26.485 de Protección Integral para Prevenir, Sancionar y Erradicar la Violencia contra las Mujeres en los ámbitos en que desarrollen sus relaciones interpersonales, 2009), (Ley XIV N.º 6 de Violencia Familiar, 2008), (Ley N.º 27.501 de Protección integral de las Mujeres, 2019), (Ley Micaela N.°27.499, 2019), entre otras.
<br> <br>
Dentro del Observatorio de Violencia Familiar y de Género se conformó en abril del año 2022 el Área de Diversidad, la misma además de compartir el objetivo de avanzar hacia la igualdad de trato de todas las personas en la sociedad, también se encargará de la recopilación y sistematización de datos e información, necesarios para generar una base de conocimientos, con el propósito de construir mensajes, divulgaciones, socializaciones, acercando la (Ley de Identidad de Género N.° 26.743, 2012), junto a términos pertenecientes al colectivo LGTBIQ+ y estudio de casos a la sociedad. <br>
Somos una política pública<br>

